    private void sortirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sortirActionPerformed
        int[] dataMasuk = new int[13];
        dataMasuk[0] = Integer.parseInt(Txt1.getText());
        dataMasuk[1] = Integer.parseInt(Txt2.getText());
        dataMasuk[2] = Integer.parseInt(Txt3.getText());
        dataMasuk[3] = Integer.parseInt(Txt4.getText());
        dataMasuk[4] = Integer.parseInt(Txt5.getText());
        dataMasuk[5] = Integer.parseInt(Txt6.getText());
        dataMasuk[6] = Integer.parseInt(Txt7.getText());
        dataMasuk[7] = Integer.parseInt(Txt8.getText());
        dataMasuk[8] = Integer.parseInt(Txt9.getText());
        dataMasuk[9] = Integer.parseInt(Txt10.getText());
        dataMasuk[10] = Integer.parseInt(Txt11.getText());
        dataMasuk[11] = Integer.parseInt(Txt12.getText());
        dataMasuk[12] = Integer.parseInt(Txt13.getText());

        if (buttonAscen.isSelected()) {
            for (int i = 0; i < dataMasuk.length - 1; i++) {
                for (int j = 0; j < dataMasuk.length - 1 - i; j++) {
                    if (dataMasuk[j] > dataMasuk[j + 1]) {
                        int temp = dataMasuk[j];
                        dataMasuk[j] = dataMasuk[j + 1];
                        dataMasuk[j + 1] = temp;
                    }
                }
            }
        } else if (buttonDescen.isSelected()) {
            for (int i = 0; i < dataMasuk.length - 1; i++) {
                for (int j = 0; j < dataMasuk.length - 1 - i; j++) {
                    if (dataMasuk[j] < dataMasuk[j + 1]) {
                        int temp = dataMasuk[j];
                        dataMasuk[j] = dataMasuk[j + 1];
                        dataMasuk[j + 1] = temp;
                    }
                }
            }
        }

        print1.setText(Integer.toString(dataMasuk[0]));
        print2.setText(Integer.toString(dataMasuk[1]));
        print3.setText(Integer.toString(dataMasuk[2]));
        print4.setText(Integer.toString(dataMasuk[3]));
        print5.setText(Integer.toString(dataMasuk[4]));
        print6.setText(Integer.toString(dataMasuk[5]));
        print7.setText(Integer.toString(dataMasuk[6]));
        print8.setText(Integer.toString(dataMasuk[7]));
        print9.setText(Integer.toString(dataMasuk[8]));
        print10.setText(Integer.toString(dataMasuk[9]));
        print11.setText(Integer.toString(dataMasuk[10]));
        print12.setText(Integer.toString(dataMasuk[11]));
        print13.setText(Integer.toString(dataMasuk[12]));

    }//GEN-LAST:event_sortirActionPerformed

